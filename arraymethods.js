/*switch*/
var grade = "F";

switch (grade) {
  case "A":
    console.log("You did great!");
    break;

  case "B":
    console.log("You did Good!");
    break;

  case "C":
    console.log("You did Okay!");
    break;

  case "D":
    console.log("You didn't do well!");
    break;

  case "F":
    console.log("You FAILED!");
    break;

  default:
    console.log("grade is not a letter Grade");
    break;
}

